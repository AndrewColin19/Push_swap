#include "utils.h"

void	ft_sa(t_pile *p)
{
	t_liste	*tmp;

	if (p->a && p->a->next)
	{
		tmp = p->a->next;
		p->a->next = p->a->next->next;
		tmp->next = p->a;
		p->a = tmp;
	}
}
