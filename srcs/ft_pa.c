#include "utils.h"

void	ft_pa(t_pile *p)
{
	t_liste	*tmp;

	if (p->b)
	{
		p->size_b--;
		p->size_a++;
		tmp = p->b->next;
		p->b->next = p->a;
		p->a = p->b;
		p->b = tmp;
	}
}
