#include "utils.h"

void	ft_pb(t_pile *p)
{
	t_liste	*tmp;

	if (p->a)
	{
		p->size_b++;
		p->size_a--;
		tmp = p->a->next;
		p->a->next = p->b;
		p->b = p->a;
		p->a = tmp;
	}
}
